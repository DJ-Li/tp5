    /**
     * 管理用户列表
     */
    public function lists()
    {
        $p = $this->request->param('p');
        if ($p < 1 || !is_numeric($p)) {
            $p = 1;
        }
        $size = 3;
        $list = $this->admin_model->get_admin_list($p, $size);
        $data = [
            'status' => 200,
            'msg' => '获取成功',
            'data' => ['list' => $list['list']],
            'pages' => $list['page'],
        ];
        return json($data);
    }

    /**
     * 用户信息
     */
    public function admin_info()
    {
        $id = request()->param('id');
        if (empty($id) || !check_id($id)){
            return json(['status' => AjaxCode::PARAM_EMPTY, 'msg' => '参数错误!']);
        }
        $admin_info = $this->admin_model->by_id_admin($id);
        $this->assign('info', $admin_info);
        return $this->fetch();
    }

