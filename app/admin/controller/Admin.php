     * 登录界面
     */
    public function login()
    {
        return $this->fetch('/login');
    }

    /**
     * 登录
     */
    public function login_post()
    {
        $post = $this->request->post();
        if (!captcha_check($post['verify']) || empty($post['verify'])) {
            return json(['status' => AjaxCode::PARAM_VALID, 'msg' => '验证码不正确!']);
        }

    }

    /**
     * 检查验证码
     */
    public function check_verify()
    {
        $verify = $this->request->post('verify');
        if (!captcha_check($verify) || $verify == '') {
            return json(['status' => AjaxCode::PARAM_VALID, 'msg' => '验证码不正确!']);
        } else {
            return json(['status' => AjaxCode::SUCCESS, 'msg' => '验证码不正确!']);
        }
    }

    public function exit_login()
    {
        Session::delete('sys_admin');
        $this->redirect(url('admin/login'));
    }
